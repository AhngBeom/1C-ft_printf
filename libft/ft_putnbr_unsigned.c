#include "libft.h"

void	ft_putnbr_unsigned(unsigned int n)
{
	char c;

	c = '0';
	if (n / 10 > 0)
		ft_putnbr_fd(n / 10, 1);
	ft_putchar_fd(c + (n % 10), 1);
}
